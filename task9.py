# Отримуємо велику основу, меншу основу і висоту трапеції від користувача
MB = float(input("Введіть довжину великої основи трапеції: "))
mb = float(input("Введіть довжину меншої основи трапеції: "))
H = float(input("Введіть висоту трапеції: "))

# Обчислюємо площу трапеції
area = 0.5 * (MB + mb) * H

# Виводимо результат
print("Площа трапеції з великою основою {}, меншою основою {} і висотою {} дорівнює {:.2f}".format(MB, mb, H, area))
